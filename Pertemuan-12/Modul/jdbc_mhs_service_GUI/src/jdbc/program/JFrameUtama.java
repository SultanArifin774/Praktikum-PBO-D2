    public int getLastId(){
        int lastId = 0;
        
        for(int i = 0; i < ListID.getSize(); i++){
            int currentId = Integer.parseInt(ListID.getElementAt(i));
            if (currentId > lastId){
                lastId = currentId;
            }
        }
        return lastId;
    }
