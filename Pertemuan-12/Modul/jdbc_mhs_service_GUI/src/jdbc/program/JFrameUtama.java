    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        int index = jListID.getSelectedIndex();
        String selected = jTextFieldEditNama.getText();
        String select = jTextFieldEditID.getText();
        
        mhs.setId(Integer.parseInt(select));
        mhs.setNama(selected);
        ListNama.setElementAt(selected, index);
        ListID.setElementAt(select, index);
        jTextFieldEditNama.setText("");
        jTextFieldEditID.setText("");
        service.update(mhs);
    }//GEN-LAST:event_jButtonEditActionPerformed

