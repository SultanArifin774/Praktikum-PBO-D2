    private void jButtonAmbilDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAmbilDataActionPerformed
        List<Mahasiswa> mahasiswaList = service.getAll();
        for (Mahasiswa mhs : mahasiswaList){
            ListID.addElement(String.valueOf(mhs.getId()));
            ListNama.addElement(mhs.getNama());
        }
        
    }//GEN-LAST:event_jButtonAmbilDataActionPerformed

    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
        String nama = jTextFieldInputNama.getText();
        int id = getLastId() + 1;
        mhs.setNama(nama);
        mhs.setId(id);
        service.add(mhs);
        
        ListID.addElement(String.valueOf(mhs.getId()));
        ListNama.addElement(mhs.getNama());
        jTextFieldInputNama.setText("");
    }//GEN-LAST:event_jButtonTambahActionPerformed

